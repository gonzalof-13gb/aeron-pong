package com.weareadaptive.pong.client.data;

public class PlayerData
{
    private int x;
    private int y;
    private int width;
    private int height;

    public int x()
    {
        return x;
    }

    public void x(final int x)
    {
        this.x = x;
    }

    public int y()
    {
        return y;
    }

    public void y(final int y)
    {
        this.y = y;
    }

    public int width()
    {
        return width;
    }

    public void width(final int width)
    {
        this.width = width;
    }

    public int height()
    {
        return height;
    }

    public void height(final int height)
    {
        this.height = height;
    }
}
