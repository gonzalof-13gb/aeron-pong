package com.weareadaptive.pong.client.data;

public class BallData
{
    public int x;
    public int y;
    public int radius;

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getRadius() {
        return radius;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setRadius(int radius) {
        this.radius = radius;
    }
}
